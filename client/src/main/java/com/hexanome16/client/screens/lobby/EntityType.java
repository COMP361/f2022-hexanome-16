package com.hexanome16.client.screens.lobby;

/**
 * Enum of possible entities in the lobby screen.
 */
public enum EntityType {
  /**
   * Own session list.
   */
  OWN_SESSION_LIST,
  /**
   * Other session list.
   */
  OTHER_SESSION_LIST,
  /**
   * Create session button.
   */
  CREATE_SESSION_BUTTON,
  /**
   * Close button.
   */
  CLOSE_BUTTON,
  /**
   * Preferences button.
   */
  PREFERENCES_BUTTON,
  /**
   * Logo.
   */
  LOGO,
  /**
   * Own header.
   */
  OWN_HEADER,
  /**
   * Other header.
   */
  OTHER_HEADER,
  /**
   * Background.
   */
  BACKGROUND,
  /**
   * Game service list.
   */
  GAME_SERVICE_LIST,
}
