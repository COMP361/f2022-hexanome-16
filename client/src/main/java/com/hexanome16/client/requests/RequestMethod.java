package com.hexanome16.client.requests;

/**
 * This enum contains all the possible HTTP request methods.
 */
public enum RequestMethod {
  /**
   * GET request method.
   */
  GET,
  /**
   * POST request method.
   */
  POST,
  /**
   * PUT request method.
   */
  PUT,
  /**
   * DELETE request method.
   */
  DELETE
}
